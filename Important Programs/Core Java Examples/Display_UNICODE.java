
public class Display_UNICODE 
{
	public static void main(String[] args) 
	{
		for (int i = 65; i <= 90; i++) 
		{
			System.out.print((char) i +"\t");	
		}
		System.out.println();
		for (int i = 2000; i <= 3000; i++) 
		{
			System.out.print((char) i +"\t");	
		}
	}
}

